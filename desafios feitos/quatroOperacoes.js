function quatroOperacoes (num1, num2) {

    console.log(num1 + "+" + num2 + "=" + (num1 + num2)) 
    console.log(num1 + "-" + num2 + "=" + (num1 - num2))
    console.log(num1 + "*" + num2 + "=" + (num1 * num2))
    console.log(num1 + "/" + num2 + "=" + (num1 / num2))

}
quatroOperacoes(10,5)
